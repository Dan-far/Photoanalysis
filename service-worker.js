// ============================================================
// ПЕРЕМЕННЫЕ НАСТРОЕК
// ============================================================

const CACHE_NAME = 'app-v1';
// const — объявление константы (переменной которую нельзя изменить).
// CACHE_NAME — имя нашего кэша в браузере.
// Кэш — это место где браузер хранит копии файлов для офлайн-работы.
// 'app-v1' — произвольное название. 
// ВАЖНО: когда обновляешь программу — меняй на 'app-v2', 'app-v3' и т.д.
// Это заставит браузер скачать новые файлы вместо старых.

const FILES_TO_CACHE = [
// const — снова константа.
// FILES_TO_CACHE — список файлов которые нужно сохранить в кэш.
// [ — начало массива (списка).

  '/',
  // '/' — корневая страница сайта (index.html).
  // Когда пользователь заходит на сайт без указания файла — это она.

  './index.html',
  // Явно указываем index.html — на случай если браузер обращается напрямую к файлу.

  './icons/icon-192.png',
  // Кэшируем иконку 192x192 — она нужна для отображения в установленном виде.

  './icons/icon-512.png'
  // Кэшируем иконку 512x512.
  // После последнего элемента запятой НЕТ.
];
// ] — конец массива.
// ; — конец строки кода (точка с запятой).
// Если у тебя есть другие файлы (CSS, картинки) — добавь их сюда же!
// Пример: '/style.css', '/logo.png'


// ============================================================
// СОБЫТИЕ: УСТАНОВКА (происходит один раз при первом посещении)
// ============================================================

self.addEventListener('install', event => {
// self — ссылка на сам service worker (как "я" для воркера).
// addEventListener — навешиваем обработчик события.
// 'install' — событие установки воркера (срабатывает один раз при первом визите).
// event => { } — стрелочная функция, код внутри выполнится когда событие произойдёт.

  event.waitUntil(
  // event.waitUntil() — говорит воркеру: "подожди пока не закончится то что внутри".
  // Без этого воркер может завершиться до того как файлы сохранятся в кэш.

    caches.open(CACHE_NAME).then(cache => {
    // caches — встроенный браузерный объект для работы с кэшем.
    // .open(CACHE_NAME) — открываем (или создаём) кэш с нашим именем 'app-v1'.
    // .then(cache => { }) — когда кэш открылся, выполни следующее.
    // cache — объект открытого кэша, с ним и будем работать.

      return cache.addAll(FILES_TO_CACHE);
      // cache.addAll() — скачивает ВСЕ файлы из списка FILES_TO_CACHE и сохраняет в кэш.
      // return — возвращаем результат чтобы waitUntil дождался окончания.
    })
  );
});
// Закрываем функцию установки.

self.skipWaiting();
// Говорим воркеру: "не жди, активируйся сразу".
// По умолчанию новый воркер ждёт пока закроются все вкладки с сайтом.
// skipWaiting() убирает это ожидание — новая версия применяется мгновенно.


// ============================================================
// СОБЫТИЕ: АКТИВАЦИЯ (срабатывает после установки)
// ============================================================

self.addEventListener('activate', event => {
// 'activate' — событие активации воркера.
// Срабатывает после install, когда воркер готов управлять страницами.
// Здесь удаляем старые кэши чтобы не занимать место на устройстве.

  event.waitUntil(
  // Снова говорим: подожди пока не закончим удаление старых кэшей.

    caches.keys().then(keys =>
    // caches.keys() — получаем список ВСЕХ имён кэшей которые есть в браузере.
    // .then(keys => ...) — когда список получен, обрабатываем его.
    // keys — массив всех имён кэшей.

      Promise.all(
      // Promise.all() — выполняем несколько операций одновременно.
      // Ждём пока ВСЕ из них завершатся.

        keys
        // Берём массив всех ключей (имён кэшей).

        .filter(key => key !== CACHE_NAME)
        // .filter() — оставляем только те ключи которые НЕ равны нашему текущему кэшу.
        // То есть отбираем старые кэши ('app-v1' когда текущий уже 'app-v2').
        // key !== CACHE_NAME — условие: ключ не равен текущему имени кэша.

        .map(key => caches.delete(key))
        // .map() — для каждого старого кэша выполняем удаление.
        // caches.delete(key) — удаляет кэш с этим именем из браузера.
      )
    )
  );

  self.clients.claim();
  // Говорим воркеру: "возьми под контроль все открытые вкладки прямо сейчас".
  // Без этого воркер начнёт работать только при следующем открытии вкладки.
});


// ============================================================
// СОБЫТИЕ: ПЕРЕХВАТ ЗАПРОСОВ (срабатывает при каждом запросе)
// ============================================================

self.addEventListener('fetch', event => {
// 'fetch' — событие перехвата сетевых запросов.
// Срабатывает КАЖДЫЙ РАЗ когда страница что-то запрашивает (файл, картинку и т.д.).
// Здесь мы решаем: брать из кэша или из интернета.

  event.respondWith(
  // event.respondWith() — подменяем ответ на запрос своим.
  // Вместо того чтобы браузер сам лез в интернет — мы контролируем что вернуть.

    caches.match(event.request).then(cached => {
    // caches.match() — ищем запрошенный файл в кэше.
    // event.request — текущий запрос (какой файл запрашивается).
    // .then(cached => ...) — когда поиск завершён.
    // cached — найденный файл из кэша (или undefined если не найден).

      return cached || fetch(event.request);
      // Если cached существует (файл есть в кэше) — возвращаем его. Интернет не нужен!
      // Если cached = undefined (файла нет в кэше) — делаем обычный запрос в интернет.
      // || — оператор "или": берём первое что не пустое.
    })
  );
});
